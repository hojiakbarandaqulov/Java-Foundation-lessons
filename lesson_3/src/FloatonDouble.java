public class FloatonDouble {
    public static void main(String[] args) {
        float p=3.14f;
        float f=1.2f;
        System.out.println(p);
        System.out.println(f);
        double d1=123.334;
        double d2=12332.23442;
        System.out.println(d1);
        System.out.println(d2);
    }
}
